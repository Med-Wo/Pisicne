int		ft_countnbr(char *str)
{
	int i;
	int count;

	i = 0;
	count = 0;
	while (str[i])
	{
		if (str[i] >= '1' && str[i] <= '9')
		{
			count++;
		}
		i++;
	}
	return (count);
}

